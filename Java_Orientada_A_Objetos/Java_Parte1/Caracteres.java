public class Caracteres {
    public static void main(String[] args) {
        
        // Char
        char caracter = 65;
        System.out.println(caracter);

        caracter = 'a';
        System.out.println(caracter);
        
        // String
        String palabra = "Hola mundo!";
        System.out.println(palabra);

    }
}
