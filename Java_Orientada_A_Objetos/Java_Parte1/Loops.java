public class Loops {
    
    public static void main(String[] args) {
        
        int contador = 1;

        // Bucle while igual a c
        while(contador <= 10){
            System.out.println(contador);
            contador++;
        }
        

        System.out.println("Bucle for");
        // Bucle for 
        for(int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }

}
